package org.openchs.web.request;

public class RuleDependencyRequest {
    private String code;
    private String hash;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getHash() {
        return hash;
    }

    public void setHash(String hash) {
        this.hash = hash;
    }
}
