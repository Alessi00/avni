package org.avni.report;

import java.util.Date;

public class CountForDay {
    private Date date;
    private long count;

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public long getCount() {
        return count;
    }

    public void setCount(long count) {
        this.count = count;
    }
}
